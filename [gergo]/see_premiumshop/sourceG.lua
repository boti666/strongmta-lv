function resourceStart(res)
	if getResourceName(res) == "see_items" then
		local items = exports.see_items:getNonStackableItems()

		for i = 1, #items do
			limitCount[items[i]] = 1
		end
	elseif source == getResourceRootElement() then
		local see_items = getResourceFromName("see_items")

		if see_items and getResourceState(see_items) == "running" then
			local items = exports.see_items:getNonStackableItems()

			for i = 1, #items do
				limitCount[items[i]] = 1
			end
		end
	end
end
addEventHandler("onClientResourceStart", getRootElement(), resourceStart)
